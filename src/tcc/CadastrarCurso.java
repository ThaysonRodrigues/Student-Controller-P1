    private void jButton9MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton9MousePressed
            int igual = 0 ;
            Funcoes con = new Funcoes();
            con.conectar();
            igual = con.Curso(campoNomeCurso.getText());
            
        if(igual > 0){    
            JOptionPane.showMessageDialog(null, "Curso ja cadastrado!");
        }else{
            Curso curso = new Curso();

            curso.setNomeCurso(campoNomeCurso.getText());

            EntityManagerFactory factory;
                factory = Persistence.createEntityManagerFactory(
                        "tccPU");
                CursoJpaController cursojpa = new CursoJpaController(factory);

            if(campoNomeCurso.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Campo n√£o preenchido:\n Nome do curso.");
            }else{
                curso.setId(0);
                cursojpa.create(curso);

                JOptionPane.showMessageDialog(null,"Curso cadastrado com sucesso!");
                campoNomeCurso.setText("");
                factory.close();
            }
        }
    }//GEN-LAST:event_jButton9MousePressed

