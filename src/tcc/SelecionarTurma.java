    public SelecionarTurma(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();    
        this.setLocationRelativeTo(null);
        
        EntityManagerFactory factory;
        factory = Persistence.createEntityManagerFactory(
                "tccPU");
        TurmaJpaController turmajpa = new TurmaJpaController(factory);
        turmas = turmajpa.findTurmaEntities();
        factory.close();
        
        comboSelecionarTurma.removeAllItems();
        
        for(Turma t : turmas){
            comboSelecionarTurma.addItem(t.getSerie());
        }
        
    }

